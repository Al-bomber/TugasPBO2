    private void tampilkanCuaca(String kota) {
        String response = cuaca.getWeatherData(kota);
        if (response != null) {
            JSONObject json = new JSONObject(response);
            String kondisiCuaca = json.getJSONArray("weather").getJSONObject(0).getString("description");
            String iconCode = json.getJSONArray("weather").getJSONObject(0).getString("icon");
            double suhu = json.getJSONObject("main").getDouble("temp") - 273.15;
            
            String suhuC = String.format("%.2f", suhu);

            outputCek.setText("Cuaca : " + kondisiCuaca + " | Suhu : " + suhuC + "Â°C");
            
            try {
                URL urlIcon = new URL("http://openweathermap.org/img/wn/" + iconCode + ".png");
                ImageIcon icon = new ImageIcon(urlIcon);
                jLabel5.setIcon(icon);
                jLabel5.setText(""); // Bersihkan teks jika ada
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }
