    private void bHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHitungActionPerformed
        int bulan = inputBulan.getSelectedIndex();
        int tahun = (int) inputTahun.getValue();
        
        if (inputBulan.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Data Bulan Belum Dipilih!!", "Gagal Menghitung", 2);
        }
        
        HitungHari perhitungan = new HitungHari(bulan, tahun);
        
        // Menampilkan hasil
        outputHari.setText(perhitungan.getJumlahHari() + " Hari");
        outputHariFirst.setText("Hari " + perhitungan.getHariPertama());
        outputHariEnd.setText("Hari " + perhitungan.getHariTerakhir());
    }//GEN-LAST:event_bHitungActionPerformed

    private void bSelisihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSelisihActionPerformed
        int hariDipilih = jCalendar1.getDayChooser().getDay();
        int bulanDipilih = inputBulan.getSelectedIndex() + 1;
        int tahunDipilih = (int) inputTahun.getValue();
        LocalDate tanggalDipilih = LocalDate.of(tahunDipilih, bulanDipilih, hariDipilih);
        
        HitungHari perhitungan = new HitungHari();
        long selisih = perhitungan.hitungSelisihHari(tanggalDipilih);
        
        // Menampilkan hasil selisih hari
        outputSelisih.setText("Selisihnya Sebanyak " + selisih + " Hari");
    }//GEN-LAST:event_bSelisihActionPerformed

