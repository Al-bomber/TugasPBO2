    private void updateCalendar() {
        if (updating) return; // Cegah loop
        updating = true;
        
        int bulan = inputBulan.getSelectedIndex() - 1;
        int tahun = (int) inputTahun.getValue();
        
        // Ambil objek Calendar dari JCalendar
        Calendar cal = jCalendar1.getCalendar();
        
        // Set bulan dan tahun pada Calendar
        cal.set(Calendar.MONTH, bulan);
        cal.set(Calendar.YEAR, tahun);
        
        // Set kalender kembali ke JCalendar untuk memastikan tampilannya diperbarui
        jCalendar1.setCalendar(cal);
        
        updating = false;
    }

    private void updateBulanTahun() {
        if (updating) return; // Cegah loop
        updating = true;
        
        Calendar cal = jCalendar1.getCalendar();
        int bulan = cal.get(Calendar.MONTH) + 1;
        int tahun = cal.get(Calendar.YEAR);
        
        inputBulan.setSelectedIndex(bulan);
        inputTahun.setValue(tahun);
        
        updating = false;
    }
